package app.coolingSystems;

public class VolumetricAirFlowRate {

	private float volumetricAirFlow;

	public VolumetricAirFlowRate(float volumetricAirFlow) {
		this.volumetricAirFlow = volumetricAirFlow;
	}
	
	public float getVolumetricAirFlow() {
		return volumetricAirFlow;
	}

	public void setVolumetricAirFlow(float volumetricAirFlow) {
		this.volumetricAirFlow = volumetricAirFlow;
	}

	
	
}
